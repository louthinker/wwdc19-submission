
/* Extension: UIColor+Grid */
/* Includes colors used in GridView. */

import UIKit

extension UIColor {
    
    struct Grid {
        
        static let Black = UIColor(red: 0.20, green: 0.20, blue: 0.20, alpha: 1.0)
        static let Blue = UIColor(red: 0.16, green: 0.68, blue: 1.0, alpha: 1.0)
        
    }
    
}
